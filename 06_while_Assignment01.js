console.log("---------------------Using While loop------------------------");
//print number 5 to 15 by incrementing 1
var i = 5;
while (i<=15) {
   console.log(i); 
   i++;
}

console.log("--------------------------------------------------------------------");

//print 40 to 50 numbers by decrementing 1
var i = 50;
while (i>=40) {
    console.log(i);
    i--;
}

